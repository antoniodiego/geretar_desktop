    private void btSalvarAlteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarAlteracoesActionPerformed
        try {
            String texto = documento.getText(0, documento.getLength());

            t.setComentario(texto);
            t.setDataModif(LocalDateTime.now());

            DAOTarefa daoT = new DAOTarefa();
            daoT.atualiza(t);

            alteracoes = false;

            btSalvarAlteracoes.setEnabled(false);

            JOptionPane op = new JOptionPane("Comentário alterado",
                    JOptionPane.INFORMATION_MESSAGE, JOptionPane.DEFAULT_OPTION, iconeConc);
            Dialog d = op.createDialog(this, "Sucesso");
            d.setModal(false);
            
            d.setVisible(true);

            System.out.println("exibiu di");
            timer.schedule(new TimerTask() {
                @Override
                public void run() {
                    System.out.println("rodando tarefa");
                  // d.setVisible(false);
                   d.dispose();
                }
            }, 2000);
            
         
        } catch (BadLocationException ex) {
            Logger.getLogger(FrameDetalhes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btSalvarAlteracoesActionPerformed

