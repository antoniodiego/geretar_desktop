    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        System.out.println("closing...");
        if (alteracoes) {
            int res = JOptionPane.showConfirmDialog(this, "Salvar alterações ?");
            switch (res) {
                case JOptionPane.OK_OPTION:
                    btSalvarAlteracoesActionPerformed(null);
                    this.dispose();
                    break;
                case JOptionPane.NO_OPTION:
                    this.dispose();
                    break;
                case JOptionPane.CANCEL_OPTION:
                    break;
                default:
                    throw new AssertionError();
            }
        } else {
            this.dispose();
        }
    }//GEN-LAST:event_formWindowClosing

    private Document documento;

    public void setTarefa(Tarefa t) {
        documento = new DefaultStyledDocument();

        try {
            documento.insertString(0, t.getComentario(), null);
        } catch (BadLocationException ex) {
            Logger.getLogger(FrameDetalhes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         documento.addDocumentListener(this);

        campoDetalhes.setDocument(documento);

        campoDetalhes.setCaretPosition(documento.getLength());

        this.t = t;

        campoTitulo.setText(t.getTitulo());
        
        btSalvarAlteracoes.setEnabled(false);
    }

